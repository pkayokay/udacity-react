  ~'npm install -g create-react-app'
  // sets up everything with no need for configuration!
  // Gives global acccess to use command 'create-react-app'

  ~'create-react-app myapp'
  // installs react, react-dom and react-scripts
  // react-scripts gives us babel, webpack, webpack-dev-server

